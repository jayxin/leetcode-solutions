// read 1d array
int* read_1d_arr(int n) {
  if (!n) return NULL;
  int *ret = (int *)malloc(sizeof(int) * n);
  for (int i = 0; i < n; i++) {
    scanf("%d", ret + i);
  }
  return ret;
}

// free 1d array
void free_1d_arr(int *arr) {
  if (!arr) {
    return;
  }
  free(arr);
}

int main(int argc, char *argv[])
{
  int n;
  scanf("%d", &n);
  int *arr = read_1d_arr(n);
  struct ListNode *h = buildList(arr, n);
  free_1d_arr(arr);
  struct ListNode *ret = reverseList(h);
  printList(ret);
  destroyList(h);
  return EXIT_SUCCESS;
}


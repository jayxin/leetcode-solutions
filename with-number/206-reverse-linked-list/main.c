/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     struct ListNode *next;
 * };
 */
struct ListNode* reverseList(struct ListNode* head) {
  struct ListNode *current = head,
                  *prev = NULL,
                  *next = NULL;
  while (current) {
    next = current->next;
    current->next = prev;
    prev = current;
    current = next;
  }

  head = prev;
  return head;
}
